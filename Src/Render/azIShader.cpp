#include "azIShader.h"
#include <Cg/cg.h>
#include "Math/azMatrix4x4.h"

//----------------------------------------------------------------------------------------------------------------------
//
//----------------------------------------------------------------------------------------------------------------------
void azIShader::Terminate()
{
	// Destroy program
	if (m_pProgram)
	{
		cgDestroyProgram(m_pProgram);
	}
}

//----------------------------------------------------------------------------------------------------------------------
//
//----------------------------------------------------------------------------------------------------------------------
CGparameter azIShader::GetParameter(azSz a_szName) const
{
    return cgGetNamedParameter(m_pProgram, a_szName);
}
