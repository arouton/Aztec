#include "azITexture.h"
#include "azImage.h"

//----------------------------------------------------------------------------------------------------------------------
//
//----------------------------------------------------------------------------------------------------------------------
azITexture::azITexture()
: m_ePixelFormat()
, m_uWidth(0)
, m_uHeight(0)
{}

//----------------------------------------------------------------------------------------------------------------------
//
//----------------------------------------------------------------------------------------------------------------------
void azITexture::Initialize(azImage const& a_rImage)
{
	m_uWidth = a_rImage.GetWidth();
	m_uHeight = a_rImage.GetHeight();
	m_ePixelFormat = a_rImage.GetPixelFormat();
}