azITexture::azITexture()
: m_ePixelFormat()
, m_uWidth(0)
, m_uHeight(0)
, m_pImage(NULL)
{}

//----------------------------------------------------------------------------------------------------------------------
//
//----------------------------------------------------------------------------------------------------------------------
void azITexture::Initialize(azUInt a_uWidth, azUInt a_uHeight, azEPixelFormat::Enum a_ePixelFormat)
{
	m_uWidth = a_uWidth;
	m_uHeight = a_uHeight;
	m_ePixelFormat = a_ePixelFormat;
}